// ==================== Language Manager ====================
window.LangManager = {
    current: localStorage.getItem('lang') || 'en',

    t(key) {
        const lang = this.current;
        return (window.translations[lang] && window.translations[lang][key]) ||
               (window.translations['en'] && window.translations['en'][key]) ||
               key;
    },

    set(lang) {
        this.current = lang;
        localStorage.setItem('lang', lang);
        this.applyAll();
    },

    applyAll() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const text = this.t(key);
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = text;
            } else {
                el.textContent = text;
            }
        });
        // Update html lang attribute
        const langMap = { en: 'en', ta: 'ta', hi: 'hi' };
        document.documentElement.lang = langMap[this.current] || 'en';

        // Update active button
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('active-lang', btn.getAttribute('data-lang') === this.current);
        });
    }
};

// Auto-apply on DOM ready
document.addEventListener('DOMContentLoaded', () => {
    window.LangManager.applyAll();
});
